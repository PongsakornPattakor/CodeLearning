// Push
const data = [1, 2, 3];
console.log(data);

data.push(100);
data.push(...[200, 300, 500]);
console.log(data);

// Pop
const cloths = ["shirt", "pants", "underwear", "bras"];
cloths.pop();
console.log(cloths);

// Shift
const furnitures = ["โต๊ะ", "เก้าอี้", "ชั้นวาง"];
furnitures.shift();
console.log(furnitures);

// Unshift
const age = [11, 25, 38, 40];
age.unshift(9);
console.log(age);
