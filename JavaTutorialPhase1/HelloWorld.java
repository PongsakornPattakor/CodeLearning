public class HelloWorld {
public static void main(String[] args) {
    int number1 = 10; //integer = ตัวแปรที่เป็นตัวเลข
    int number2 = 20;
    System.out.print("Sum of number is ");// println จะเว้นบรรทัด
    System.out.println(number1*number2);// print จะไม่เว้นบรรทัด
    // Syntax = ไวยกรณ์เขียนของแต่ละภาษา เช่น การแสดงของ java คือ System.out.print();
}
}